-- Function: get_districts_geojson
CREATE OR REPLACE FUNCTION public.get_districts_geojson()
 RETURNS jsonb
 LANGUAGE sql
 STABLE
AS $function$
  select jsonb_build_object(
    'type', 'FeatureCollection',
    'features', jsonb_agg(
      jsonb_build_object(
        'type', 'Feature',
        'geometry', ST_AsGeoJSON(geom)::jsonb,
        'properties', jsonb_build_object(
          'id', id,
          'name_en', name_en,
          'province', province,
          'adm2_code', adm2_code
        )
      )
    )
  )
  from district;
$function$
;

-- Function: get_hazard_events_geojson
CREATE OR REPLACE FUNCTION public.get_hazard_events_geojson()
 RETURNS jsonb
 LANGUAGE sql
 STABLE
AS $function$
  select jsonb_build_object(
    'type', 'FeatureCollection',
    'features', jsonb_agg(
      jsonb_build_object(
        'type', 'Feature',
        'geometry', ST_AsGeoJSON(geom)::jsonb,
        'properties', jsonb_build_object(
          'id', id,
          'hazard', hazard,
          'severity', severity,
          'title', title,
          'starts_at', starts_at
        )
      )
    )
  )
  from hazard_event
  where geom is not null;
$function$
;

-- Function: get_district_lonlat
CREATE OR REPLACE FUNCTION public.get_district_lonlat(district_id uuid)
 RETURNS TABLE(lon double precision, lat double precision)
 LANGUAGE sql
 STABLE
AS $function$
  select ST_X(centroid), ST_Y(centroid) from district where id = district_id;
$function$
;

-- Function: get_district_hazards
CREATE OR REPLACE FUNCTION public.get_district_hazards(p_district_id uuid, p_limit integer DEFAULT 20)
 RETURNS TABLE(id uuid, hazard text, severity text, title text, starts_at timestamp with time zone, source text)
 LANGUAGE sql
 STABLE
AS $function$
  select he.id, he.hazard, he.severity, he.title, he.starts_at, he.source
  from hazard_event he, district d
  where d.id = p_district_id
    and he.geom is not null
    and ST_DWithin(he.geom::geography, d.geom::geography, 50000)
  order by he.starts_at desc nulls last
  limit p_limit;
$function$
;

