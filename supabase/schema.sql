-- View: district_flood_risk_geojson
CREATE OR REPLACE VIEW public."district_flood_risk_geojson" AS
SELECT d.id AS district_id,
    d.name_en,
    ff.forecast_date,
    ff.risk_level,
    ff.river_discharge,
    st_asgeojson(d.geom) AS geometry_json
   FROM (district d
     LEFT JOIN LATERAL ( SELECT f.id,
            f.district_id,
            f.forecast_date,
            f.river_discharge,
            f.river_discharge_mean,
            f.risk_level,
            f.source,
            f.fetched_at
           FROM flood_forecast f
          WHERE ((f.district_id = d.id) AND (f.forecast_date >= CURRENT_DATE))
          ORDER BY f.forecast_date
         LIMIT 1) ff ON (true));

-- View: station_health
CREATE OR REPLACE VIEW public."station_health" AS
SELECT s.id AS station_id,
    s.name,
    s.kind,
    s.district_id,
    s.is_simulated,
    lr.recorded_at AS last_transmission_at,
    lr.battery_voltage,
    lr.rssi,
    lr.water_level,
    lr.rainfall,
    lr.temperature,
    lr.flow_rate,
        CASE
            WHEN (lr.recorded_at IS NULL) THEN 'offline'::text
            WHEN ((now() - lr.recorded_at) > '00:30:00'::interval) THEN 'offline'::text
            WHEN ((now() - lr.recorded_at) > '00:15:00'::interval) THEN 'degraded'::text
            WHEN ((lr.battery_voltage IS NOT NULL) AND (lr.battery_voltage < 11.0)) THEN 'degraded'::text
            ELSE 'online'::text
        END AS status,
    st_x(s.geom) AS lon,
    st_y(s.geom) AS lat
   FROM (station s
     LEFT JOIN LATERAL ( SELECT sr.id,
            sr.station_id,
            sr.recorded_at,
            sr.water_level,
            sr.rainfall,
            sr.temperature,
            sr.battery_voltage,
            sr.rssi,
            sr.flow_rate,
            sr.is_simulated
           FROM station_reading sr
          WHERE (sr.station_id = s.id)
          ORDER BY sr.recorded_at DESC
         LIMIT 1) lr ON (true));

